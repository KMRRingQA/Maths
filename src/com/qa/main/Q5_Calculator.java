package com.qa.main;

public class Q5_Calculator {

	int add(int number1, int number2) {
		return number1 + number2;
	}

	double divide(double number1, double number2) {
		return number1 / number2;
	}

	int multiply(int number1, int number2) {
		return number1 * number2;
	}

	int subtract(int number1, int number2) {
		return number1 - number2;
	}

}
